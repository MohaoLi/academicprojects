    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblWorkrequst.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : workQueue.getWorkRequestList()) {
            if (wr instanceof AuthenticateTransactionWorkRequest) {
                Object[] row = new Object[4];
                row[0] = wr;
                row[1] = wr.getSender();
                row[2] = wr.getReceiver();
                row[3] = wr.getStatusString();
                model.addRow(row);
            }
        }

    }

