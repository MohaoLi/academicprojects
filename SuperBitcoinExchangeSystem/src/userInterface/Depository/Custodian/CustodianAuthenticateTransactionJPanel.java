    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        // TODO add your handling code here:
        int row = tblWorkrequst.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        AuthenticateTransactionWorkRequest authenticateTransactionWorkRequest = (AuthenticateTransactionWorkRequest) tblWorkrequst.getValueAt(row, 0);
        if(depositoryEnterprise.getBitcoinList().keySet().contains(authenticateTransactionWorkRequest.getBitcoin())){
            authenticateTransactionWorkRequest.setReceiver(userAccount);
            authenticateTransactionWorkRequest.setResolveDate(new Date());
            authenticateTransactionWorkRequest.setStatusString("Completed");
            txtSeller.setText("");
            txtBuyer.setText("");
            txtBitcoinPrivateKey.setText("");
            populateTable();
            JOptionPane.showMessageDialog(null, "Verify successfully.", "Information", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "This transaction is fake.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnVerifyActionPerformed

    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        int row = tblWorkrequst.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        AuthenticateTransactionWorkRequest authenticateTransactionWorkRequest = (AuthenticateTransactionWorkRequest) tblWorkrequst.getValueAt(row, 0);
        txtSeller.setText(authenticateTransactionWorkRequest.getSeller().getName());
        txtBuyer.setText(authenticateTransactionWorkRequest.getBuyer().getName());
        txtBitcoinPrivateKey.setText(authenticateTransactionWorkRequest.getBitcoin().getPrivateKey());
    }//GEN-LAST:event_btnCheckActionPerformed


